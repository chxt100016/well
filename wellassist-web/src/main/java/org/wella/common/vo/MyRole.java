package org.wella.common.vo;

public class MyRole {
    private int add = 0;
    private int edit = 0;
    private int del = 0;
    private int view = 0;

    public MyRole() {
    }

    public int getAdd() {
        return this.add;
    }

    public void setAdd(int add) {
        this.add = add;
    }

    public int getEdit() {
        return this.edit;
    }

    public void setEdit(int edit) {
        this.edit = edit;
    }

    public int getDel() {
        return this.del;
    }

    public void setDel(int del) {
        this.del = del;
    }

    public int getView() {
        return this.view;
    }

    public void setView(int view) {
        this.view = view;
    }
}
